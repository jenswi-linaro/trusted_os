#ifndef MUTEX_H
#define MUTEX_H

#define MUTEX_UNLOCKED	0
#define MUTEX_LOCKED	1

#ifndef ASM

#include <stdint.h>
#include <stdbool.h>

struct mutex {
	uint32_t state;
};

#define MUTEX_INITIALIZER { MUTEX_UNLOCKED }

void mutex_lock(struct mutex *m);
bool mutex_trylock(struct mutex *m);
void mutex_unlock(struct mutex *m);

#endif /*ASM*/

#endif /*MUTEX_H*/
